import { RequestHandler } from "express";
import { User } from "../models/User";

export const checkDuplicateUsernameOrEmail: RequestHandler = (req, res, next) => {
  console.log("checkDuplicateUsernameOrEmail", req.body.username, req.body.email)
  // Username
  User.findOne({
    username: req.body.username
  }).exec((err, user) => {
    if (err) {
      res.status(500).send({ message: err });
      return;
    }

    if (user) {
      res.status(400).send({ message: "Failed! Username is already in use!" });
      return;
    }

    // Email
    User.findOne({
      email: req.body.email
    }).exec((err, user) => {
      if (err) {
        res.status(500).send({ message: err });
        return;
      }

      if (user) {
        res.status(400).send({ message: "Failed! Email is already in use!" });
        return;
      }

      next();
    });
  });
};
